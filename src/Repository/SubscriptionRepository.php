class SubscriptionRepository extends ServiceEntityRepository
{
    public function __construct(ManagerRegistry $registry)
    {
        parent::__construct($registry, Subscription::class);
    }

    public function save(Subscription $entity, bool $flush = false): void
    {
        $this->getEntityManager()->persist($entity);

        if ($flush) {
            $this->getEntityManager()->flush();
        }
    }

    public function remove(Subscription $entity, bool $flush = false): void
    {
        $this->getEntityManager()->remove($entity);

        if ($flush) {
            $this->getEntityManager()->flush();
        }
    }

    public function findTypes(): array
    {
        $query = $this->createQueryBuilder('s')
            ->select('s.type')
            ->andWhere('s.isActive = :val')
            ->setParameter(':val', true)
            ->orderBy('s.id', 'ASC')
            ->getQuery()
            ->getResult();
        
        $arr = array_column($query, 'type');    

        $getValues = array_combine($arr, $arr);
        $getValues['Other'] = 'Other';

        return $getValues;

    }


